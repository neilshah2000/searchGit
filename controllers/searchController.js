"use strict";

var myApp = angular.module('searchModule', []);
myApp.controller('searchController', ['$scope', '$http', function($scope, $http) {
    // event to send the search request to Github back end
    $scope.sendSearch = function() {
        //remove non-alphanumeric characters
        $scope.searchValue = $scope.searchValue.replace(/[^a-z0-9+]/gi, '');
        // console.log($scope.searchValue);
        $http.get('https://api.github.com/search/repositories?q=' + $scope.searchValue).then(function successCallback(response) {
            //response can be null
            if (response.data.items.length !== 0) {
                $scope.searchData = response.data.items;
                $scope.searchProperties = Object.getOwnPropertyNames($scope.searchData[0]);
            } else {
                console.log($scope.searchValue + ' returns zero results');
            }
        }, function errorCallback(response) {
            console.log(response.statusText);
            console.log("error getting data from Github");
        });
    };

    $scope.enterPressed = function(key) {
        if(key.which === 13){
          // console.log('enter pressed');
          $scope.sendSearch();
        }  
    };

    $scope.outputResults = function(obj) {
        return JSON.stringify(obj, null, 4);
    };

    // event to expand the accordion when the search result is clicked
    $scope.expandAccordion = function($event) {
        var button;
        button = $event.target;
        button.classList.toggle('active');
        button.nextElementSibling.classList.toggle('show');
    };
}]);


